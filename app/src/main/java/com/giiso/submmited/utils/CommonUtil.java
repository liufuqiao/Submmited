    public static File saveBitmap(Bitmap mBitmap, String filePath) {
        File f = new File(filePath);
        if (!f.exists()) {
            f.mkdirs();
        }
        if (f.exists()) {
            f.delete();
        }
        FileOutputStream fOut = null;
        try {
            f.createNewFile();
            fOut = new FileOutputStream(f);

            mBitmap.compress(Bitmap.CompressFormat.JPEG, 100, fOut);
            fOut.flush();
            fOut.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
        return f;
    }

